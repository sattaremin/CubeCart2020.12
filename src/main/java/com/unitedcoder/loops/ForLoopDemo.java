package com.unitedcoder.loops;

public class ForLoopDemo {
    public static void main(String[] args) {

        for (int i = 1; i <= 1000; i++) {
           System.out.print(i+" ");
        }
        System.out.println();
        for ( int i=1000;i>=1;i--){
            System.out.print(i+" ");
        }
        System.out.println();
        for (int j=0;j<=100;j=j+2){
            System.out.print(j+" ");
        }
    }
}
